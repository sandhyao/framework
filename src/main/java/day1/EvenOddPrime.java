package day1;

public class EvenOddPrime {

	public static void main(String[] args) {
		int number=5;
		
		if(number%2==0)
		{
			System.out.println("the number is even");
		}
		else if(number%2==1)
		{
			System.out.println("the number is odd");
		}
		else 
		{
			System.out.println("the number is prime");
		}
		
	}
	

}
